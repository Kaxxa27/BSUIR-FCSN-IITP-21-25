BEFORE INSERT ON Students
FOR EACH ROW
DECLARE 
    max_id NUMBER;
BEGIN
    IF :NEW.ID IS NULL THEN
        SELECT MAX(ID)
        INTO max_id
        FROM Students;
        
        IF max_id IS NOT NULL THEN
            :NEW.ID := max_id + 1;
        ELSE 
            :NEW.ID := 1;
        END IF;
    END IF;
END;
