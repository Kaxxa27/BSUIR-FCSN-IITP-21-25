DROP TABLE Students_LOG;
CREATE TABLE Students_LOG (
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    ACTION VARCHAR2(10), -- Тип действия (INSERT, UPDATE, DELETE)
    STUDENT_ID NUMBER, -- Идентификатор студента
    STUDENT_NAME VARCHAR2(100), -- Имя студента
    St_Groups_ID NUMBER, -- Идентификатор группы
    LOG_TIMESTAMP TIMESTAMP -- Метка времени аудита
);

DROP TRIGGER students_logger_trigger;
CREATE OR REPLACE TRIGGER students_logger_trigger
AFTER INSERT OR UPDATE OR DELETE ON Students
FOR EACH ROW
DECLARE
    action VARCHAR2(10);
BEGIN
    IF INSERTING THEN
        action := 'INSERT';
        INSERT INTO Students_LOG (ACTION, STUDENT_ID, STUDENT_NAME, St_Groups_ID, LOG_TIMESTAMP)
        VALUES (action, :NEW.ID, :NEW.NAME, :NEW.St_Groups_ID, SYSTIMESTAMP);
    ELSE 
        IF UPDATING THEN
            action := 'UPDATE';
            INSERT INTO Students_LOG (ACTION, STUDENT_ID, STUDENT_NAME, St_Groups_ID, LOG_TIMESTAMP)
            VALUES (action, :NEW.ID, :NEW.NAME, :NEW.St_Groups_ID, SYSTIMESTAMP);
        ELSIF DELETING THEN
            action := 'DELETE';
            INSERT INTO Students_LOG (ACTION, STUDENT_ID, STUDENT_NAME, St_Groups_ID, LOG_TIMESTAMP)
            VALUES (action, :OLD.ID, :OLD.NAME, :OLD.St_Groups_ID, SYSTIMESTAMP);
        END IF;
    END IF;
END;
/
DROP PROCEDURE restore_students_from_log;
CREATE OR REPLACE PROCEDURE restore_students_from_log(
    restore_time TIMESTAMP DEFAULT NULL,
    time_offset INTERVAL DAY TO SECOND DEFAULT NULL
) 
AS
    restore_timestamp TIMESTAMP;
BEGIN
    IF restore_time IS NULL AND time_offset IS NULL THEN
        RAISE_APPLICATION_ERROR(-20001, 'Не указано время восстановления или временное смещение.');
    END IF;

    IF restore_time IS NOT NULL THEN
        restore_timestamp := restore_time;
    ELSE
        restore_timestamp := SYSTIMESTAMP - time_offset;
    END IF;

    FOR log_record IN (SELECT * FROM Students_LOG WHERE LOG_TIMESTAMP <= restore_timestamp) 
    LOOP
        IF log_record.ACTION = 'INSERT' THEN
            IF log_record.STUDENT_ID IS NOT NULL THEN
                DELETE FROM Students WHERE ID = log_record.STUDENT_ID; 
            END IF;
            INSERT INTO Students (ID, NAME, St_Groups_ID)
            VALUES (log_record.STUDENT_ID, log_record.STUDENT_NAME, log_record.St_Groups_ID);
        ELSIF log_record.ACTION = 'UPDATE' THEN
            UPDATE Students
            SET NAME = log_record.STUDENT_NAME, St_Groups_ID = log_record.St_Groups_ID
            WHERE ID = log_record.STUDENT_ID;
        ELSIF log_record.ACTION = 'DELETE' THEN
            DELETE FROM Students WHERE ID = log_record.STUDENT_ID;
        END IF;
    END LOOP;
END;
/

CREATE OR REPLACE TRIGGER update_groups_cval_trigger
 AFTER INSERT OR UPDATE OR DELETE ON Students
 FOR EACH ROW
BEGIN 
    IF UPDATING THEN
        UPDATE St_Groups
        SET C_VAL = C_VAL + 1
        WHERE id = :NEW.St_Groups_ID;

        UPDATE St_Groups
        SET C_VAL = C_VAL - 1
        WHERE id = :OLD.St_Groups_ID;
    END IF;

    IF INSERTING THEN
        UPDATE St_Groups
        SET C_VAL = C_VAL + 1
        WHERE id = :NEW.St_Groups_ID;
    END IF;

    IF DELETING THEN
        UPDATE St_Groups
        SET C_VAL = C_VAL - 1
        WHERE id = :OLD.St_Groups_ID;
    END IF;
END;
